let nums = [1, 2, 3, 4];

let doubled = nums.map((num) => num * 2);

console.log(doubled); // Output: [2, 4, 6, 8]

// This code demonstrates the use of the map() method in JavaScript. 
// The map() method creates a new array by applying a provided function to each element of the original array. 
// In this example, each number in the 'nums' array is multiplied by 2, resulting in a new array 'doubled' containing [2, 4, 6, 8].