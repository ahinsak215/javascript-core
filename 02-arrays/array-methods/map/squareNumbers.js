let nums = [2, 3, 4];

let squares = nums.map((n) => n * n);

console.log(squares); // Output: [4, 9, 16]

// This code uses the map() method to create a new array 'squares' that contains the squares of each number in the original 'nums' array. 
// Each element 'n' in the 'nums' array is squared (multiplied by itself) to form the new array.