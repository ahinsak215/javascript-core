let items = ["Pen", "Book"];
items.push("Notebook");
items.pop();

console.log(items);

// This code demonstrates the use of push and pop methods on an array in JavaScript.
// The push method adds "Notebook" to the end of the items array,
// and the pop method removes the last element from the array.
// Finally, it logs the modified array, which will be ["Pen", "Book"].