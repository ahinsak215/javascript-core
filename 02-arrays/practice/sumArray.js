let nums = [1, 2, 3, 4];
let sum = 0;

for (let i = 0; i < nums.length; i++) {
  sum += nums[i];
}

console.log(sum); // Output: 10

// This code calculates the sum of all numbers in an array.
// It initializes a sum variable to 0, then iterates through each element in the nums array,
// adding each element's value to the sum.
// Finally, it logs the total sum, which is 10 in this case.