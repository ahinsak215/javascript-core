let marks = 75;

if (marks >= 90) {
  console.log("Grade A");
} else if (marks >= 75) {
  console.log("Grade B");
} else if (marks >= 50) {
  console.log("Grade C");
} else {
  console.log("Fail");
}

// This code assigns a grade based on the marks obtained.
// If marks are 90 or above, it's Grade A; 75-89 is Grade B; 50-74 is Grade C; below 50 is Fail.