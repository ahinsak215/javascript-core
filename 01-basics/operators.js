let x = 10;
let y = 3;

console.log(x + y); // arithmetic
console.log(x > y); // comparison
console.log(x === y); // strict equality
console.log(x && y); // logical
console.log(typeof x); // typeof
console.log(x += y); // assignment
console.log(++x); // increment
console.log(--y); // decrement
console.log(x % y); // modulus
console.log(x ** y); // exponentiation
