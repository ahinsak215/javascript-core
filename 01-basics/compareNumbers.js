let a = 10;
let b = 20;

console.log(a > b); // true if a is greater than b
console.log(a < b); // true if a is less than b
console.log(a >= b); // true if a is greater than or equal to b
console.log(a <= b); // true if a is less than or equal to b
console.log(a == b); // true if a is equal to b
console.log(a != b); // true if a is not equal to b
console.log(a === b); // true if a is strictly equal to b
console.log(a !== b); // true if a is not strictly equal to b
