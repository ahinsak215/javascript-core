let user = {
  name: "Anshu",
  address: {
    city: "Jodhpur",
    state: "Rajasthan",
  },
};

console.log(user.address.city); // Output: Jodhpur

// This code demonstrates how to create and access nested objects in JavaScript. 
// The 'user' object contains another object 'address' as one of its properties. 
// We can access the nested property 'city' using dot notation: user.address.city.