let users = [
  { name: "A", age: 20 },
  { name: "B", age: 25 },
];

for (let user of users) {
  console.log(user.name); // Output: A B
}

// This code demonstrates how to work with an array of objects in JavaScript. 
// The 'users' array contains multiple user objects, each with 'name' and 'age' properties. 
// We use a for...of loop to iterate through the array and log each user's name to the console.