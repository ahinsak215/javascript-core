let key = "email";

let user = {
  name: "Anshu",
};

user[key] = "anshu@gmail.com";

console.log(user); // Output: { name: 'Anshu', email: 'anshu@gmail.com' }

// This code demonstrates how to add dynamic keys to an object in JavaScript. 
// The variable 'key' holds the property name, which is then used to add a new property to the 'user' object.